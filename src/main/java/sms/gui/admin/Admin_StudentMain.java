    private void fetchStudentData() {
        String query = "SELECT id, name, programName FROM students"; 

        try (Connection connection = DatabaseConnection.getConnection(); PreparedStatement preparedStatement = connection.prepareStatement(query); ResultSet resultSet = preparedStatement.executeQuery()) {
            String[] columnNames = {"ID", "Name", "Program"};
            DefaultTableModel showStudent = new DefaultTableModel(columnNames, 0);
            while (resultSet.next()) {
                int id = resultSet.getInt("id");
                String name = resultSet.getString("name");
                String programName = resultSet.getString("programName");
                showStudent.addRow(new Object[]{id, name, programName});
            }
            StudentList.setModel(showStudent);

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }

