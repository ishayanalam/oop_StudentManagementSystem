    private void searchStudentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchStudentActionPerformed
        // error checked -- zariaa
        try {
            if (getIDforSearch.getText().trim().isEmpty()) {
                javax.swing.JOptionPane.showMessageDialog(this, "Please enter a Student ID");
                return;
            }
            // code by sadiqah
            currentStudentID = Integer.parseInt(getIDforSearch.getText().trim());
            Admin_StudentView currentStudentView = new Admin_StudentView(currentStudentID);
            currentStudentView.setVisible(true);
            this.dispose();
            
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please enter a valid Student ID");
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error");
        }


    }//GEN-LAST:event_searchStudentActionPerformed

