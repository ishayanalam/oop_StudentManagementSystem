    private void loadProgramBasedOnDegreeAndDepartment() {
        String selectedDegree = (String) degreeType.getSelectedItem();
        String selectedDepartment = (String) department.getSelectedItem();
        //error handel if none is selected
        if (selectedDegree != null && !selectedDegree.equals("Select Degree Type") &&
            selectedDepartment != null && !selectedDepartment.equals("Select Department")) {

            String query = "SELECT program FROM academic WHERE degree = '" + selectedDegree + "' AND department = '" + selectedDepartment + "'";

            try (Connection connection = DatabaseConnection.getConnection();
                 Statement statement = connection.createStatement();
                 ResultSet resultSet = statement.executeQuery(query)) {

                // ager ta delete 
                program.removeAllItems();
                program.addItem("Select Program");
                while (resultSet.next()) {
                    String programName  = resultSet.getString("program"); 
                    program.addItem(programName);
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please select both degree and department.");
        }
    }
    
 
    
