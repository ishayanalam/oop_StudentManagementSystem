     private void loadDegreeNameInComboBox() {
        String query = "SELECT DISTINCT degree FROM academic"; 

        try (Connection connection = DatabaseConnection.getConnection();
             Statement statement = connection.createStatement();
             ResultSet resultSet = statement.executeQuery(query)) {
            degreeType.addItem("Select Degree Type");
            while (resultSet.next()) {
                String degreeTypeValue = resultSet.getString("degree");
                degreeType.addItem(degreeTypeValue);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this,e.getMessage());
        }
    }
    private void loadDeparmentNameInComboBox() {
        String query = "SELECT DISTINCT department FROM academic";

        try (Connection connection = DatabaseConnection.getConnection();
             Statement statement = connection.createStatement();
             ResultSet resultSet = statement.executeQuery(query)) {

            department.addItem("Select Department");

            while (resultSet.next()) {
                String departmentValue = resultSet.getString("department"); 
                department.addItem(departmentValue);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
